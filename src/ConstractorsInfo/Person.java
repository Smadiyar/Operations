package ConstractorsInfo;

public class Person {
	
	
	private String name;
	private String lastName;
	private int age;
	
	public Person(String name, String lastName, int age) {
		
		this.name=name;
		this.lastName=lastName;
		this.age=age;
		
	}
	
	public void setName() {
		this.name=name;
	}
	
	public void setlastNAme() {
		this.lastName=lastName;
	}
	
	public void setage() {
		this.age=age;
	}

	
	public String getName() {
		return name;
	}
	
	public String getlastNAme() {
		return lastName;
	}
	
	public int getage() {
		return age;
	}

	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
